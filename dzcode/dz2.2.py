a = int(input("enter a 5 digit number:"))
b = a % 10  # 12345 % 10 = 5
c = a // 10  # 12345 // 10 = 1234
g = c % 10  # 1234 % 10 = 4
g1 = b * 10 + g  # 5 * 10 + 4 = 54
s = c // 10  # 1234 // 10 = 123
w = s % 10  # 123 % 10 = 3
g2 = g1 * 10 + w  # 54 * 10 + 3 = 543
h = s // 10  # 123 // 10 = 12
q = h % 10  # 12 % 10 = 2
g3 = g2 * 10 + q  # 543 * 10 + 2
y = h // 10  # 12 // 10 = 1
reversed_result = g3 * 10 + y  # 5432 * 10 + 1 = 54321
print(reversed_result)  # вывод результата 54321 (к примеру)
